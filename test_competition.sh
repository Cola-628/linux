#!/bin/bash

echo "开始资源竞争测试..."

# 循环执行15次连接/断开操作
for i in {1..15}
do
    echo "===================================="
    echo "第 $i 次连接/断开"
    echo "===================================="
    
    # 启动客户端（后台运行）
    sudo ./client &
    
    # 记录客户端进程ID
    CLIENT_PID=$!
    echo "客户端进程ID: $CLIENT_PID"
    
    # 等待2秒，让客户端完成三次握手
    sleep 2
    
    # 杀死客户端进程
    sudo kill $CLIENT_PID
    echo "已杀死客户端进程"
    
    # 等待1秒，让服务器处理断开事件
    sleep 1
done

echo "===================================="
echo "测试完成！"
echo "===================================="